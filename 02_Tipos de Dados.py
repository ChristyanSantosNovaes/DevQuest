#Exemplo de Tipos de Dados

numero_inteiro = 10
numero_flutuante = 3.14
numero_complexo = 1 + 2j
print(f"Inteiro: {numero_inteiro}, Tipo: {type(numero_inteiro)}")
print(f"Flutuante: {numero_flutuante}, Tipo: {type(numero_flutuante)}")
print(f"Complexo: {numero_complexo}, Tipo: {type(numero_complexo)}")

print("---------------------------------------------------------------------------------")

#Exemplo de Tipo Booleano:

verdadeiro = True
falso = False

print(f"Verdadeiro: {verdadeiro}, Tipo: {type(verdadeiro)}")
print(f"Falso: {falso}, Tipo: {type(falso)}")

print("---------------------------------------------------------------------------------")

#Exemplo de Tipo String:

mensagem_simples = 'Olá, Python!'
mensagem_dupla = "Python é incrível!"
mensagem_multilinhas = """Esta é uma string
que ocupa várias linhas.
"""

print(f"Simples: {mensagem_simples}, Tipo: {type(mensagem_simples)}")
print(f"Dupla: {mensagem_dupla}, Tipo: {type(mensagem_dupla)}")
print(f"Multilinhas: {mensagem_multilinhas}, Tipo: {type(mensagem_multilinhas)}")